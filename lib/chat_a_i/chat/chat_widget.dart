                                                widget!.conversationId,
                                              )
                                              .order('created_at',
                                                  ascending: true),
                                        );
                                        _shouldSetState = true;
                                        _model.messages = _model.messagesResult!
                                            .toList()
                                            .cast<AiMessagesRow>();
                                        safeSetState(() {});
                                        safeSetState(() {
                                          _model.promptTextFieldTextController
                                              ?.clear();
                                        });
                                        await _model
                                            .conversationListViewScrollController
                                            ?.animateTo(
                                          _model
                                              .conversationListViewScrollController!
                                              .position
                                              .maxScrollExtent,
                                          duration: Duration(milliseconds: 200),
                                          curve: Curves.ease,
                                        );
                                      } else {
                                        ScaffoldMessenger.of(context)
                                            .showSnackBar(
                                          SnackBar(
                                            content: Text(
                                              getJsonField(
                                                _model.aiResponse,
                                                r'''$.error''',
                                              ).toString(),
                                              style: TextStyle(
                                                color:
                                                    FlutterFlowTheme.of(context)
                                                        .primaryText,
                                              ),
                                            ),
                                            duration:
                                                Duration(milliseconds: 4000),
                                            backgroundColor:
                                                FlutterFlowTheme.of(context)
                                                    .error,
                                          ),
                                        );
                                      }

                                      _model.isLoading = false;
                                      safeSetState(() {});
                                      if (_shouldSetState) safeSetState(() {});
                                    },
                                  ).animateOnPageLoad(animationsMap[
                                      'iconButtonOnPageLoadAnimation']!),
                                ],
                              ),
                              if (responsiveVisibility(
                                context: context,
                                tablet: false,
                                tabletLandscape: false,
                                desktop: false,
                              ))
                                wrapWithModel(
                                  model: _model.mainBottomNavModel,
                                  updateCallback: () => safeSetState(() {}),
                                  child: MainBottomNavWidget(),
                                ),
                            ],
                          ),
                        ),
                      ),
                    ],
                  );
                },
              ),
            ),
          ),
        ),
      ),
    );
  }
